<script>
document.addEventListener("DOMContentLoaded", function () {
    fetch("http://localhost/acuas-main/admin/fetch_reviews.php")
        .then(response => {
            if (!response.ok) {
                throw new Error(`HTTP error! Status: ${response.status}`);
            }
            return response.json();
        })
        .then(data => {
            if (!data || !Array.isArray(data.reviews)) {
                throw new Error("Reviews data is not in the expected format");
            }

            const carouselInner = document.getElementById("testimonialCarouselInner");
            carouselInner.innerHTML = "";

            data.reviews.forEach((review, index) => {
                const carouselItem = document.createElement("div");
                carouselItem.classList.add("carousel-item", "text-center", "p-4");
                if (index === 0) {
                    carouselItem.classList.add("active");
                }

                carouselItem.innerHTML = `
                    <div class="review-box" style="border: 1px solid #ddd; padding: 30px; margin: 20px auto; max-width: 600px; text-align: left;">
                        <p>${review.comments}</p>
                    </div>
                    <div class="d-block">
                        <h4 class="text-dark">${review.fullname}</h4>
                        <div class="d-flex justify-content-center text-secondary">
                            ${'★'.repeat(review.rating)}${'☆'.repeat(5 - review.rating)}
                        </div>
                    </div>
                `;

                carouselInner.appendChild(carouselItem);
            });
        })
        .catch(error => console.error("Error fetching reviews:", error));
});
</script>

